    private void Btn_SubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_SubmitActionPerformed

        if (txt_Name.getText().isEmpty() || txt_RoomNumber.getText().isEmpty() || ComboBox_Location.getSelectedItem().equals("SELECT LOCATION")||
            ComboBox_TravelBy.getSelectedItem().equals("SELECT TRAVEL BY") || txt_NumberOfPasengers.getText().isEmpty() ||
            ComboBox_DropPickup.getSelectedItem().equals("SELECT DROP OR PICKUP")|| txt_From.getText().isEmpty() || txt_From.getText().isEmpty()||
            ((JTextField) date_Depature.getDateEditor().getUiComponent()).getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "CAN'T EXIT EMPTY VALUES ");
        }
        else {

            String name = txt_Name.getText();
            String roomNumber = txt_RoomNumber.getText();
            String location = ComboBox_Location.getSelectedItem().toString();
            String travelBy = ComboBox_TravelBy.getSelectedItem().toString();
            String noOfPasenger = txt_NumberOfPasengers.getText();
            String dropPickup = ComboBox_DropPickup.getSelectedItem().toString();
            String from = txt_From.getText();
            String to = txt_To.getText();
            Date depature = date_Depature.getDate();

            try {

                String query = "INSERT INTO add_request_guest (Name,RoomNumber,Location,TravelBy,NoOfPasengers,DropPickup,Req_From,Req_To,Depature)"
                + "VALUES ('"+name+"','"+roomNumber+"','"+location+"','"+travelBy+"','"+noOfPasenger+"','"+dropPickup+"','"+from+"','"+to+"','"+depature+"')";

                pst = (com.mysql.jdbc.PreparedStatement) con.prepareStatement(query);
                int i = pst.executeUpdate();

                if (i  > 0) {
                    JOptionPane.showMessageDialog(null, "DATA SUCCESSFULLY INSETED");

                } else {
                    JOptionPane.showMessageDialog(null, "DATA IS NOT INSERTED");
                }

                //tableload();
            }
            catch (Exception e)
            {
                System.out.println("exception " + e);
                JOptionPane.showMessageDialog(null, e);

            }
        }

        txt_Name.setText("");
        txt_RoomNumber.setText("");
        //ComboBox_Location.set
        //ComboBox_TravelBy.setText("");
        txt_NumberOfPasengers.setText("");
        //ComboBox_DropPickup.settext("");
        txt_From.setText("");
        txt_To.setText("");
        //date_Depature.setText("");

    }//GEN-LAST:event_Btn_SubmitActionPerformed


