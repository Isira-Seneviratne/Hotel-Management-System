    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        int resultTest = JOptionPane.showConfirmDialog(rootPane, "DO YOU WANT TO UPDATE THIS RECOED !");

        if (resultTest == 0) {
            String rid = jLabel1.getText();
            String user = txt_user.getText();
            String date1 = ((JTextField) jDateChooser1.getDateEditor().getUiComponent()).getText();
            String date2 = ((JTextField) jDateChooser2.getDateEditor().getUiComponent()).getText();
            String cost = txt_cost.getText();
            // String vJoindate = lbl_VehicleID.getText();
            String aditionalCost = txt_adtional_cost.getText();
            String handover = txt_handoverPerson.getText();

            String sqlUpdate = "UPDATE repair SET Request_User = '" + user + "',Repair_Date='" + date1 + "',Repair_Return_Date='" + date2 + "' , Repair_Cost='" + cost + "',"
                    + " Repair_Aditional_Cost='" + aditionalCost + "' ,Handover_User='" + handover + "' WHERE RID = '" + rid + "'";

            try {

                pst = (com.mysql.jdbc.PreparedStatement) con.prepareStatement(sqlUpdate);

                pst.executeUpdate();

                tableload();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());

            }

        }
    }//GEN-LAST:event_btn_updateActionPerformed

