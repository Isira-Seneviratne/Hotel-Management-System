    private void Btn_SubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_SubmitActionPerformed
        //String location = ComboBox_Location.getSelectedItem().toString();
        if (txt_name.getText().isEmpty() || txt_employeeNumber.getText().isEmpty() || ComboBox_Department.getSelectedItem().equals("SELECT DEPARTMENT")||
            txt_mobile.getText().isEmpty()||ComboBox_Manager.getSelectedItem().equals("SELECT MANAGER ") || txt_location.getText().isEmpty()||
            ComboBox_TravelBy.getSelectedItem().equals("SELECT TRAVEL METHORD") || txt_noOfPasenger.getText().isEmpty()||
            ComboBox_DropPickup.getSelectedItem().equals("SELECT OPTION")|| txt_from.getText().isEmpty() || txt_to.getText().isEmpty() ||
            ((JTextField) jDateChooser1.getDateEditor().getUiComponent()).getText().isEmpty() || txt_reason.getText().isEmpty())

        {JOptionPane.showMessageDialog(null, "CAN'T EXIT EMPTY VALUES "); }

        else {

            String name = txt_name.getText();
            String employeeNumber = txt_employeeNumber.getText();
            String department = ComboBox_Department.getSelectedItem().toString();
            String mobileNumber = txt_mobile.getText();
            String reportingManger = ComboBox_Manager.getSelectedItem().toString();
            String location = txt_location.getText();
            String travelBy = ComboBox_TravelBy.getSelectedItem().toString();
            String noOfPasengers = txt_noOfPasenger.getText();
            String dropPickup =ComboBox_DropPickup.getSelectedItem().toString();
            String froms = txt_from.getText();
            String tos = txt_to.getText();
            Date depatures = jDateChooser1.getDate();

            String reason1  = txt_to.getText();

            try {

                String query = "INSERT INTO add_request_staff (Name,Employee_Number,Department,Mobile_Number,Reporting_Manager,Location,TravelBy,NoOfPasengers,DropPickup,Req_From,Req_To,Depature,Reason)"
                + "VALUES ('"+name+"','"+employeeNumber+"','"+department+"','"+mobileNumber+"','"+reportingManger+"','"+location+"','"+travelBy+"','"+noOfPasengers+"','"+dropPickup+"',"
                + "'"+froms+"','"+tos+"','"+depatures+"','"+reason1+"')";

                pst = (com.mysql.jdbc.PreparedStatement) con.prepareStatement(query);
                int i = pst.executeUpdate();

                if (i  > 0) {
                    JOptionPane.showMessageDialog(null, "DATA SUCCESSFULLY INSETED");

                } else {
                    JOptionPane.showMessageDialog(null, "DATA IS NOT INSERTED");
                }

                //tableload();
            }
            catch (HeadlessException | SQLException e)
            {
                System.out.println("exception " + e);
                JOptionPane.showMessageDialog(null, e);

            }

            txt_name.setText("");
            txt_employeeNumber.setText("");
            txt_mobile.setText("");
            txt_location.setText("");
            txt_noOfPasenger.setText("");
            txt_from.setText("");
            txt_to.setText("");
            // jDateChooser1.setText("");
            txt_to.setText("");

        }

    }//GEN-LAST:event_Btn_SubmitActionPerformed

