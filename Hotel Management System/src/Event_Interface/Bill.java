    private void tbbillMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbbillMouseClicked

        try {
            int row = tbbill.getSelectedRow();
            String Table_click = (tbbill.getModel().getValueAt(row, 0).toString());
            String sql = "select * from event_bill where Billno='" + Table_click + "'";
            pst = con.prepareStatement(sql);
            rs = (ResultSet) pst.executeQuery(sql);
            if (rs.next()) {

                String add1 = rs.getString("Billno");
                billbox.setText(add1);

                String add2 = rs.getString("eventId");
                eventIdbox1.setText(add2);

                String add3 = rs.getString("Date");
                java.util.Date date;
                try {
                    date = new SimpleDateFormat("yyyy-MM-dd").parse(add3);

                    datebox1.setDate(date);
                } catch (ParseException ex) {

                }

                String add4 = rs.getString("extracharge");
                extrachargebox.setText(add4);

                String add5 = rs.getString("hotelcharge");
                hotelchargebox.setText(add5);

                String add6 = rs.getString("service_charge");
                servicechargebox.setText(add6);

                String add7 = rs.getString("total");
                totalbox.setText(add7);

            }

        } catch (SQLException e) {

            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_tbbillMouseClicked

