    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {

            if (billbox.getText().isEmpty() || eventIdbox1.getText().isEmpty()
                    || ((JTextField) datebox1.getDateEditor().getUiComponent()).getText().isEmpty()
                    || extrachargebox.getText().isEmpty() || hotelchargebox.getText().isEmpty() || servicechargebox.getText().isEmpty() || totalbox.getText().isEmpty()) {

                JOptionPane.showMessageDialog(null, "Enter Valid Inputs");

            }

            String sql = "Insert into event_bill (Billno,eventId,date,extracharge,hotelcharge,service_charge,total) values(?,?,?,?,?,?,?)";
            pst = con.prepareStatement(sql);

            pst.setString(1, billbox.getText());
            pst.setString(2, eventIdbox1.getText());

            pst.setString(3, ((JTextField) datebox1.getDateEditor().getUiComponent()).getText());
            pst.setString(4, extrachargebox.getText());
            pst.setString(5, hotelchargebox.getText());
            pst.setString(6, servicechargebox.getText());
            pst.setString(7, totalbox.getText());

            pst.execute();

            JOptionPane.showMessageDialog(null, "Record Saved");

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);

        }

        update_table();
        clear_table();

    }//GEN-LAST:event_jButton1ActionPerformed

