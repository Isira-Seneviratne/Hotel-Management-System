    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {

            if (eventIdbox3.getText().isEmpty() || typebox3.getSelectedItem().equals("type") || ((JTextField) datebox4.getDateEditor().getUiComponent()).getText().isEmpty()
                    || paxbox3.getText().isEmpty() || Costbox3.getText().isEmpty()) {

                JOptionPane.showMessageDialog(null, "Enter Valid Inputs");

            }

            String sql = "Insert into event_event(event_Id,event_type,date,pax,cost) values(?,?,?,?,?)";
            pst = con.prepareStatement(sql);

            pst.setString(1, eventIdbox3.getText());
            String value = typebox3.getSelectedItem().toString();
            pst.setString(2, value);
            pst.setString(3, ((JTextField) datebox4.getDateEditor().getUiComponent()).getText());
            pst.setString(4, paxbox3.getText());
            pst.setString(5, Costbox3.getText());

            pst.execute();

            JOptionPane.showMessageDialog(null, "Record Saved");

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);

        }

        update_table();
        clear_table();

    }//GEN-LAST:event_jButton1ActionPerformed

