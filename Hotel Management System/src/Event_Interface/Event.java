    private void et3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_et3MouseClicked
        try {
            int row = et3.getSelectedRow();
            String Table_click = (et3.getModel().getValueAt(row, 0).toString());
            String sql = "select * from event_event where event_Id='" + Table_click + "'";
            pst = con.prepareStatement(sql);
            rs = (ResultSet) pst.executeQuery(sql);
            if (rs.next()) {

                String add1 = rs.getString("event_Id");
                eventIdbox3.setText(add1);

                String add2 = rs.getString("event_type");
                typebox3.setSelectedItem(add2);

                String add3 = rs.getString("date");
                java.util.Date date;
                try {
                    date = new SimpleDateFormat("yyyy-MM-dd").parse(add3);

                    datebox4.setDate(date);
                } catch (ParseException ex) {

                }
                String add4 = rs.getString("pax");
                paxbox3.setText(add4);
                String add5 = rs.getString("cost");
                Costbox3.setText(add5);

            }

        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, e);
        }

    }//GEN-LAST:event_et3MouseClicked

