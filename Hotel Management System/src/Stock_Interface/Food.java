    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        try
        {
            int row = jTable1.getSelectedRow();
            String Table_click = jTable1.getModel().getValueAt(row, 0).toString();
            pst = con.prepareStatement("select * from food_items where Food_Item_Id='"+Table_click+"'");
            rs = pst.executeQuery();
            
            while (rs.next())
            {
                String add1 = rs.getString("Food_Item_Id");
                jLabel10.setText(add1);

                String add2 = rs.getString("Food_Name");
                jTextField1.setText(add2);

                String add3 = rs.getString("Quantity");
                jTextField3.setText(add3);

                String add4 = rs.getString("Unit");
                jComboBox2.setSelectedItem(add4);

                String add5 = rs.getString("Vender");
                jComboBox1.setSelectedItem(add5);

                String add6 = rs.getString("Purchased_Date");
                
                try
                {
                    java.util.Date date =  new SimpleDateFormat("yyyy-MM-dd").parse(add6);
                    jDateChooser1.setDate(date);
                } catch (ParseException e) {
                    JOptionPane.showMessageDialog(this, "Unable to parse date.", "Error", JOptionPane.ERROR_MESSAGE);
                }
                
                try
                {
                    java.util.Date date =  new SimpleDateFormat("yyyy-MM-dd").parse(add6);
                    jDateChooser2.setDate(date);
                } catch (ParseException e) {
                    JOptionPane.showMessageDialog(this, "Unable to parse date.", "Error", JOptionPane.ERROR_MESSAGE);
                }
                
                String add8 = rs.getString("Price");
                jTextField2.setText(add8);
            }
        }
        catch(HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, "An error occurred while retrieving the records.", "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jTable1MouseClicked

