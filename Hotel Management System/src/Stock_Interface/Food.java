    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(jTextField1.getText().isEmpty()
                || jComboBox2.getSelectedItem().toString().isEmpty()
                || jTextField3.getText().isEmpty())
        {
             JOptionPane.showMessageDialog(null,"One or more fields are empty. Enter the missing data.",
                     "Missing data", JOptionPane.ERROR_MESSAGE);
        }
        else
        {
            String food_name = jTextField1.getText();
            boolean food_name_validation = ValidationHRMS.isLetter(food_name);
            String unit = jComboBox2.getSelectedItem().toString();
            String qty = jTextField3.getText();
            boolean qty_validation = ValidationHRMS.isNumeric(qty);
            String purchased_date = ((JTextField)jDateChooser1.getDateEditor().getUiComponent()).getText();
            String expiary_date = ((JTextField)jDateChooser2.getDateEditor().getUiComponent()).getText();
            String vender = jComboBox1.getSelectedItem().toString();
            String price = jTextField2.getText();
            boolean price_validation = ValidationHRMS.isNumeric(price);

            float totValue;
            
            try {
                totValue = Float.parseFloat(price) * Integer.parseInt(qty);
            } catch(NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "An invalid value has been entered for the price and/or quantity fields."
                        + "Enter a valid value.", "Invalid Value", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            try {
                if(food_name_validation) {
                    if(qty_validation) {
                        if(price_validation) {
                            String w = "INSERT INTO food_items(Food_Name, Quantity, Unit, Vender, Purchased_Date, Expiary_Date, Price) values ('"+food_name+"', '"+qty+"', '"+unit+"', '"+vender+"', '"+purchased_date+"', '"+expiary_date+"', '"+totValue+"')";
                            pst = con.prepareStatement(w);
                            pst.execute();
                            JOptionPane.showMessageDialog(null, "Item details inserted successfully.", "Success",
                                    JOptionPane.INFORMATION_MESSAGE);
                            loadTable();
                        }
                        else {
                            JOptionPane.showMessageDialog(null, "Enter a price.", "Missing price",
                                    JOptionPane.ERROR_MESSAGE);
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "Enter a quantity.", "Missing quantity",
                                JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Enter a valid item name.", "Invalid item name", JOptionPane.ERROR_MESSAGE);
                }
            } catch (SQLException e) {
                 JOptionPane.showMessageDialog(null, "An error occurred while inserting the new item.", "Error",
                         JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

